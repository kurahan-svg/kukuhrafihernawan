    private void loadData() {
    // Konfigurasi Hibernate
    Configuration cfg = new Configuration().configure().addAnnotatedClass(Proyek.class);
    SessionFactory factory = cfg.buildSessionFactory();
    Session session = factory.openSession();

    try {
        // Mulai transaksi
        session.beginTransaction();

        // Ambil semua data dari tabel Proyek
        List<Proyek> proyekList = session.createQuery("FROM Proyek").list();

        // Buat model tabel baru
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new String[]{"Nama Proyek", "Lokasi", "Tanggal Mulai", "Tanggal Selesai"});

        // Tambahkan data ke model tabel
        for (Proyek proyek : proyekList) {
            model.addRow(new Object[]{
                proyek.getNamaproyek(),
                proyek.getLokasi(),
                proyek.getTanggalmulai(),
                proyek.getTanggalselesai()
            });
        }

        // Set model ke JTable
        jTable1.setModel(model);

        session.getTransaction().commit();
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat memuat data proyek.");
    } finally {
        session.close();  // Tutup sesi
        factory.close();  // Tutup sesi factory
    }
}
