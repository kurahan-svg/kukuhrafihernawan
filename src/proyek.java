    private void button1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button1ActionPerformed
        // TODO add your handling code here:
        String namaProyek = jTextField1.getText();

    // Check if the project name is not empty
    if (namaProyek == null || namaProyek.trim().isEmpty()) {
        JOptionPane.showMessageDialog(null, "Nama proyek tidak boleh kosong!");
        return;
    }

    // Configure Hibernate
    Configuration cfg = new Configuration().configure().addAnnotatedClass(Proyek.class);
    SessionFactory factory = cfg.buildSessionFactory();
    Session session = factory.getCurrentSession();

    try {
        // Begin transaction
        Transaction transaction = session.beginTransaction();

        // Retrieve the Proyek object by name
        Proyek proyekToDelete = (Proyek) session.createQuery("FROM Proyek WHERE namaProyek = :namaProyek")
                                                .setParameter("namaProyek", namaProyek)
                                                .uniqueResult();

        if (proyekToDelete != null) {
            // If the project is found, delete it
            session.delete(proyekToDelete);
            transaction.commit();  // Commit transaction
            JOptionPane.showMessageDialog(null, "Proyek berhasil dihapus!");
            loadData();
        } else {
            JOptionPane.showMessageDialog(null, "Proyek tidak ditemukan!");
        }
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat menghapus proyek.");
    } finally {
        session.close();  // Close session
        factory.close();  // Close factory
    }
    }//GEN-LAST:event_button1ActionPerformed

